package mybatis.vo;

public class SurveyVO {
	private String laptop_MonitorSize,
					laptop_OS,
					program_Name,
					status,
					laptop_Weight,
					laptop_Price;

	public String getLaptop_MonitorSize() {
		return laptop_MonitorSize;
	}

	public void setLaptop_MonitorSize(String laptop_MonitorSize) {
		this.laptop_MonitorSize = laptop_MonitorSize;
	}

	public String getLaptop_OS() {
		return laptop_OS;
	}

	public void setLaptop_OS(String laptop_OS) {
		this.laptop_OS = laptop_OS;
	}

	public String getProgram_Name() {
		return program_Name;
	}

	public void setProgram_Name(String program_Name) {
		this.program_Name = program_Name;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getLaptop_Weight() {
		return laptop_Weight;
	}

	public void setLaptop_Weight(String laptop_Weight) {
		this.laptop_Weight = laptop_Weight;
	}

	public String getLaptop_Price() {
		return laptop_Price;
	}

	public void setLaptop_Price(String laptop_Price) {
		this.laptop_Price = laptop_Price;
	}
	
					
}
